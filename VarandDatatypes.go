package main

import (
	"fmt"
)

func main() {
	Var()
}

func Var() {

	// ALL DATA TYPES IN GO LANG

	// 1. string
	var str string = "Sting"

	// 2. int/int8/int16/int32/int64
	var Num int = 10

	// 3. float/32/64
	var flt float64 = 3500.485

	// 4. boolean -  true || false
	var yesNo bool = false

	fmt.Println("string", str, "\nNumber int: ", Num, "\nflt:", flt, "\nBoolean: ", yesNo)

	// var short hand assign ( name:= )

}
